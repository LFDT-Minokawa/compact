//! Native Rust runtime for contracts emitted by `compactc --rust`.
//!
//! This crate is the Rust counterpart to the TypeScript package
//! `@midnight-ntwrk/compact-runtime`. Code generated by
//! `compactc --rust` depends on it; you typically do not consume it
//! directly — instead, you compile a `.compact` source with
//! `compactc --rust` and let the emitted `lib.rs` import from
//! `compact_runtime::*`.
//!
//! # User-facing entry points
//!
//! End users of `compactc --rust` interact with `compact-runtime`
//! through a small surface:
//!
//! - [`Contract`](#)-shaped structs in generated code call into:
//!   - [`ConstructorContext`] / [`CircuitContext`] (input contexts)
//!   - [`ConstructorResult`] / [`CircuitResults`] (output envelopes)
//!   - [`WitnessContext`] (read-only context handed to witness impls)
//!   - [`CompactError`] (the error type returned by every circuit)
//!   - [`compact_assert!`] (Compact's `assert(cond, "msg")` macro)
//!   - [`check_runtime_version!`] (compile-time pin against `compactc`)
//! - The [`Witnesses<PS>`](#) trait, declared in generated code, with
//!   one method per `witness foo(…): T;` declaration in the source.
//! - The [`std_lib`] module — [`Maybe<T>`](std_lib::Maybe),
//!   [`Bytes<N>`](std_lib::Bytes), [`std_lib::pad`],
//!   [`std_lib::disclose`], the Jubjub / EC helpers, and the
//!   width-typed `decode_*` helpers used by the generated ledger view.
//!
//! For a full walked-through example see
//! [`doc/rust-codegen-user-guide.md`](https://github.com/LFDT-Minokawa/compact/blob/main/doc/rust-codegen-user-guide.md).
//!
//! # Internal structure
//!
//! At the top of this file is a curated prelude — re-exports of the
//! upstream Midnight types (`AlignedValue`, `Fr`, `JubjubPoint`,
//! `StateValue`, `Op`, …) under stable `compact_runtime::*` paths.
//! Generated code only ever names types through this prelude; the
//! codegen's `type-rust` mapping in
//! [`compiler/rust-passes-types.ss`](https://github.com/LFDT-Minokawa/compact/blob/main/compiler/rust-passes-types.ss)
//! always emits `compact_runtime::Foo`, never `midnight_xyz::Foo`. That
//! lets us replace upstream symbols without regenerating fixtures.
//!
//! The Rust-side facade modules add the Compact-level types that don't
//! exist upstream:
//!
//! - `context` / `results` / `witness` / `error` (private submodules,
//!   re-exported at the crate root) — circuit / constructor envelopes
//!   and the unified [`CompactError`] type.
//! - [`version`] — the [`COMPACT_RUNTIME_VERSION`] constant and
//!   [`check_runtime_version!`] macro.
//! - [`builders`] — `StateValue` constructors (`new_cell`, `new_map`,
//!   `new_merkle_tree`, `new_list`, `new_cell_bounded_uint`, …) the
//!   codegen calls to seed initial ledger field values.
//! - [`op_builder`] — typed VM op-program builders. Generated circuit
//!   bodies chain `OpProgramVerify::new().dup(…).idx_at_index(…)
//!   .push(…).ins(…).build()` or the parallel `OpProgramGather` to
//!   assemble a transcript.
//! - [`query`] — `query_for_read` / `query_for_verify` thin wrappers
//!   over `QueryContext::query` that force the correct
//!   [`ResultMode`].
//! - [`std_lib`] — the Compact standard library: `Counter`, `Maybe<T>`,
//!   `Bytes<N>`, `pad`, `disclose`, `persistentHash`, Jubjub primitives,
//!   Merkle path helpers, OpaqueString, the width-typed decoders.
//!
//! # Versioning
//!
//! [`COMPACT_RUNTIME_VERSION`] is exposed as a `&'static str` constant
//! and re-exposed by `compactc --runtime-version`. Every generated
//! `lib.rs` opens with `compact_runtime::check_runtime_version!(...)`
//! so a runtime/compiler mismatch surfaces as a compile-time error.
//!
//! # Crate-level docs vs user guide
//!
//! This crate-root doc covers the runtime API surface; for a full
//! end-to-end Compact-to-Rust walkthrough (compile, wire, witness,
//! drive circuits, troubleshoot), read the user guide linked above.

