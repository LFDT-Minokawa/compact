//
// map_lambda_fixture.compact byte-parity test — validates the Iter 7
// follow-up (non-identity lambda in map()).
//
// Drives the generated map-lambda-fixture crate through initial_state()
// and asserts the serialized ContractState matches the TS reference
// fixture captured by fixtures/capture-map-lambda-fixture.mjs.
//
// Purpose: validate the `(* mbits ...)` + `(downcast-unsigned ...)` IR
// lowerings inside a `map(fn, iterable)` lambda body. The Compact
// source constructor doubles each element of a literal `[1, 2, 3]` via
// `(x * 2) as Uint<64>` — the emitter must produce
// `[((1).wrapping_mul(2)) as u64, ...]` so the on-chain state holds
// `[2u64, 4u64, 6u64]` packed into a single AlignedValue.

use compact_contract_map_lambda_fixture::Contract;
use compact_runtime::*;
use midnight_serialize::tagged_serialize;
use midnight_storage::storage::HashMap;
use tests_e2e_rust::SmallFixtureTsReference;

fn fixture() -> SmallFixtureTsReference {
    SmallFixtureTsReference::load(concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/fixtures/map-lambda-fixture-ts-state.json"
    ))
}

fn ctor_ctx() -> ConstructorContext<()> {
    ConstructorContext {
        initial_private_state: (),
        empty_zswap_local_state: ZswapLocalState::default(),
        cost_model: INITIAL_COST_MODEL.clone(),
        gas_limit: None,
    }
}

/// Build a ContractState envelope around a freshly minted ChargedState.
/// map_lambda_fixture exports a single circuit `ping`, so the
/// operations map must register one entry under that name to match
/// the TS-side initialState() output.
fn make_envelope(
    data: ChargedState<midnight_storage::DefaultDB>,
) -> ContractState<midnight_storage::DefaultDB> {
    let mut operations: HashMap<EntryPointBuf, ContractOperation, midnight_storage::DefaultDB> =
        HashMap::new();
    operations = operations.insert(
        EntryPointBuf(b"ping".to_vec()),
        ContractOperation::new(None),
    );
    ContractState {
        data,
        operations,
        maintenance_authority: ContractMaintenanceAuthority::default(),
        balance: Default::default(),
    }
}

#[test]
fn map_lambda_fixture_init_byte_parity() {
    let ts_ref = fixture();
    let contract: Contract<(), NoWitnesses> = Contract::new(NoWitnesses);
    let result = contract.initial_state(ctor_ctx()).expect("initial_state");

    let envelope = make_envelope(result.current_contract_state.clone());
    let mut buf = Vec::new();
    tagged_serialize(&envelope, &mut buf).expect("tagged_serialize");

    let ts_bytes = ts_ref.after_init.state_bytes();
    assert_eq!(
        buf,
        ts_bytes,
        "Rust state bytes differ from TS reference\n\nRust ({} B): {}\n\nTS   ({} B): {}",
        buf.len(),
        hex::encode(&buf),
        ts_bytes.len(),
        hex::encode(&ts_bytes),
    );
}
